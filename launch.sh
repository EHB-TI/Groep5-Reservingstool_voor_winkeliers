#!/bin/sh

MINIMUMRAM="512M"
MAXIMUMRAM="1G"
SERVICE="Reservingstool_voor_winkeliers.jar"
JAVA_EXECUTOR="java"

while true
do
echo "Start server"
$JAVA_EXECUTOR -Xms$MINIMUMRAM -Xmx$MAXIMUMRAM -XX:+UseG1GC -XX:+ParallelRefProcEnabled -XX:MaxGCPauseMillis=200 -XX:+UnlockExperimentalVMOptions -XX:+DisableExplicitGC -XX:+AlwaysPreTouch -XX:G1NewSizePercent=30 -XX:G1MaxNewSizePercent=40 -XX:G1HeapRegionSize=8M -XX:G1ReservePercent=20 -XX:G1HeapWastePercent=5 -XX:G1MixedGCCountTarget=4 -XX:InitiatingHeapOccupancyPercent=15 -XX:G1MixedGCLiveThresholdPercent=90 -XX:G1RSetUpdatingPauseTimePercent=5 -XX:SurvivorRatio=32 -XX:+PerfDisableSharedMem -XX:MaxTenuringThreshold=1 -jar $SERVICE
echo "Restarting..."
sleep 5
done
done